// O que sao operadores em Javascript?
//Sao simbolos especiais que envolvem um ou mais operandos com a finalidade de produzir um determinado resultado 

//OPERADOR DE ATRIBUIÇAO
//O operador de atribuiçao basico e o sinal de igual (=). Ele atribui valor a uma variavel:

var x; // declaraçao da variavel x

x=5; // Essa operaçao atribui a x o valor de 5;

console.log(x);

console.log('-----------2-----------');

//OPERADOR DE ATRIBUIÇAO COMPOSTA COM ADIÇAO (+=)

x += 10;

console.log(x);

console.log('-----------3-----------');

//OPERADOR DE ATRIBUIÇAO COMPOSTA COM SUBTRAÇAO (-=)

x -= 5;

console.log(x);

console.log('-----------4-----------');

//OPERADOR DE ATRIBUIÇAO COMPOSTA COM MULTIPLICAÇAO (*=)

x *= 2;

console.log(x);

console.log('-----------5-----------');

//OPERADOR DE ATRIBUIÇAO COMPOSTA COM DIVISAO (/=)

x /= 2;

console.log(x);

console.log('-----------6-----------');

//OPERADOR DE ATRIBUIÇAO COMPOSTA COM RESTO DA DIVISAO (%=)

x %= 2;

console.log(x);





